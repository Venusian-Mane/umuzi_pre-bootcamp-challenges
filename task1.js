let x = 0;
let y = 1;
// this will simply print out the values of x and y to the console
console.log(`x is equal to ${x}, and y is equal to ${y}`);

x = x + 3;
y = y + x;
// this will print out new x and y variable values to the console. Y will always be itself plus the value of X
console.log(`The new x is equal to ${x}, and the new y is equal to ${y}`);
